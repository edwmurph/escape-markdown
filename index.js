const map = {
  '*': '\\*',
  '#': '\\#',
  '(': '\\(',
  ')': '\\)',
  '[': '\\[',
  ']': '\\]',
  _: '\\_',
  '\\': '\\\\',
  '+': '\\+',
  '-': '\\-',
  '`': '\\`',
  '=': '\=',
  '<': '&lt;',
  '>': '&gt;',
  '&': '&amp'
};

module.exports = (string) => string.replace(/[\*\(\)\[\]\+\\_\-`=#<>]/g, m => map[m]);
